function [ varargout ] = spatialAutoCorr(varargin )
%PATIALAUTOCORRELATION Conducts spatial auto correlations similar to those seen in Hafting 2005
% Spatial auto correlation consists of multiple auto correlations run on a pair of ratemaps. The rate maps are slid
% accross each other and correlations are run for every offset. One or two arguments are returned -
% if only one is requested this is the autocorrelation. If two are requested the first is the
% autocorrelation and the second is a matrix indicating the number of overlapping bins used to
% calculated each value.
% Returns [autoCorrelation] a matrix
% or [autoCorrelation, nBinsOverlap]
%
%This code works by selecting blocks of cells out of two (or one) ratemaps and then comparing the blocks using corrcoef (also have
%tried to directly impliment the Moser's code). This yields
%the value of a single pixel in the spatial autocorrelation. The other pixels are generated by moving the selected blocks across each
%other then rerunning the correlation process etc. The two blocks of cells are defined by in
%x and y coordinates by selection from and too numbers. These identify the chunk of the ratemap this is selcted to make each
%block. The selection from and too numbers are generated using a mesh grid like process with padding after.
%
%Offset positions with a small number (useOverlapGreatThan) of overlaping bins are ignored
%Rate maps don't have to be the same size
%
%Takes:
%drawFileA & B standard structure carrying drawFile information
%
%NB If I need to speed this up at some point could use conv2 with padding to quickly multiply two
%matrices together


% --------------------------------------------------------------------------------------------------------------------------
% --- VARIABLES AND PARSE ARGUMENTS ----------------------------------------------------------------------------------------
% ---------------------------------------------------------------------------------------------------------------------------
warning off;
% useOverlapGreatThan=20; %Only positions with more than this number of bins overlapping will be used Hafting = 20


if length(varargin)==1
    %Do autocorrelation
    rateMapA=varargin{1};
    rateMapB=varargin{1};
elseif length(varargin)==2
    %Do correlation
    rateMapA=varargin{1};
    rateMapB=varargin{2};
else
    error('Too many arguments.');
end

% ----------------------------------------------------------------------------------------------------------------------------
% --- FUNCTION BODY ----------------------------------------------------------------------------------------------------------
% ----------------------------------------------------------------------------------------------------------------------------
%Replace NaNs in the ratemap with zero
rateMapA(isnan(rateMapA))=0;
rateMapB(isnan(rateMapB))=0;


%Deterimine size of ratemap
xSizeA=size(rateMapA,2);
ySizeA=size(rateMapA,1);
xSizeB=size(rateMapB,2);
ySizeB=size(rateMapB,1);
xSizeJoint=xSizeA+xSizeB-1;
ySizeJoint=ySizeA+ySizeB-1;

% ------------------------------------------------------------------------------------------------------------------------------
% --- FIGURE OUT BITS OF MATRIX TO SELECT --------------------------------------------------------------------------------------
% ------------------------------------------------------------------------------------------------------------------------------


tempA=1:xSizeA;
aMeshXSelectionFrom=tempA(ones(ySizeJoint,1),:);
aMeshXSelectionFrom=[ones(ySizeJoint, xSizeB-1), aMeshXSelectionFrom]; %Starts with a set of ones then increase to xMax

tempA=(1:ySizeA)';
aMeshYSelectionFrom=tempA(:,ones(1,xSizeJoint));
aMeshYSelectionFrom=[ones(ySizeB-1, xSizeJoint); aMeshYSelectionFrom]; %Starts with a set of ones then increase to yMax

tempA=1:xSizeA;
aMeshXSelectionTo=tempA(ones(ySizeJoint,1),:);
aMeshXSelectionTo=[aMeshXSelectionTo, ones(ySizeJoint, xSizeB-1).*xSizeA]; %Moves from 1 to maxX, then set of maxX

tempA=(1:ySizeA)';
aMeshYSelectionTo=tempA(:,ones(1,xSizeJoint));
aMeshYSelectionTo=[aMeshYSelectionTo ;ones(ySizeB-1, xSizeJoint).*ySizeA]; %Moves from 1 to maxY, then set of maxY
clear tempA;


tempB=1:xSizeB;
bMeshXSelectionFrom=tempB(ones(ySizeJoint,1),:);
bMeshXSelectionFrom=fliplr([ones(ySizeJoint, xSizeA-1), bMeshXSelectionFrom]); %Starts with a set of ones then increase to xMax

tempB=(1:ySizeB)';
bMeshYSelectionFrom=tempB(:,ones(1,xSizeJoint));
bMeshYSelectionFrom=flipud([ones(ySizeA-1, xSizeJoint); bMeshYSelectionFrom]); %Starts with a set of ones then increase to yMax

tempB=1:xSizeB;
bMeshXSelectionTo=tempB(ones(ySizeJoint,1),:);
bMeshXSelectionTo=fliplr([bMeshXSelectionTo, ones(ySizeJoint, xSizeA-1).*xSizeB]); %Moves from 1 to maxX, then set of maxX

tempB=(1:ySizeB)';
bMeshYSelectionTo=tempB(:,ones(1,xSizeJoint));
bMeshYSelectionTo=flipud([bMeshYSelectionTo ;ones(ySizeA-1, xSizeJoint).*ySizeB]); %Moves from 1 to maxY, then set of maxY
clear tempB;


% -----------------------------------------------------------------------------------------------------------------------------
% --- CORRELATIONS ------------------------------------------------------------------------------------------------------------
% -----------------------------------------------------------------------------------------------------------------------------
%NB There is code for two different ways of calculating correlation below - one is an implentation
%of the Mosers' code (with correction) and the other just uses corrcoef. They seem to be identical
%will use the Mosers' code though.

%Now loop round running and storing correlations each time
autoCorrelation=zeros(size(aMeshXSelectionFrom)); %Create matrix to store autocorrelation results
nBinsOverlapping=zeros(size(aMeshXSelectionFrom)); %Keep a record of the number of bins used to conduct the correlation

% Code for Moser implementation
rateMapASqr=rateMapA.^2;
rateMapBSqr=rateMapB.^2;

for n=1:prod(size(aMeshXSelectionFrom))
    selA=rateMapA(aMeshYSelectionFrom(n):aMeshYSelectionTo(n),aMeshXSelectionFrom(n):aMeshXSelectionTo(n));
    selB=rateMapB(bMeshYSelectionFrom(n):bMeshYSelectionTo(n),bMeshXSelectionFrom(n):bMeshXSelectionTo(n));
    nBins=prod(size(selA));

    %Mehtod 1 - use built in corrcoef function
    %     tempCorr=corrcoef(selA(:), selB(:));
    %     if isnan(tempCorr) autoCorrelation(n)=0; else autoCorrelation(n)=tempCorr(2); end

    %Method 2 - implement Moser code
    selASqr=rateMapASqr(aMeshYSelectionFrom(n):aMeshYSelectionTo(n),aMeshXSelectionFrom(n):aMeshXSelectionTo(n));
    selBSqr=rateMapBSqr(bMeshYSelectionFrom(n):bMeshYSelectionTo(n),bMeshXSelectionFrom(n):bMeshXSelectionTo(n));

    %NB. Am pretty sure there was an error in the description of the following formula in Hafting
    %2005 I'm using the ammended version of the formula here ( on bottom line in both of the square
    %root terms the subterms should be subtracted not multiplied)
    autoCorrelation(n)= (  nBins * sum(selA(:).*selB(:)) - (sum(selA(:)) * sum(selB(:)))  ) / ...
        (  sqrt(nBins*sum(selASqr(:)) - (sum(selA(:)))^2) * ...
        sqrt(nBins*sum(selBSqr(:)) - (sum(selB(:)))^2)  );

    nBinsOverlapping(n)=nBins;
end


% autoCorrelation(nBinsOverlapping<=useOverlapGreatThan)=0; %Set bins with too little overlap to be equal to 0
varargout{1}=autoCorrelation;
if nargout==1
    return
elseif nargout==2
    varargout{2}=nBinsOverlapping;
else
    error('To many or too few output arguments. This function can return one or two.');
end


